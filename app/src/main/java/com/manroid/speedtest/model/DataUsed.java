package com.manroid.speedtest.model;

public class DataUsed {

    private String date;
    private String dataMobile;
    private String dataWifi;
    private String total;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDataMobile() {
        return dataMobile;
    }

    public void setDataMobile(String dataMobile) {
        this.dataMobile = dataMobile;
    }

    public String getDataWifi() {
        return dataWifi;
    }

    public void setDataWifi(String dataWifi) {
        this.dataWifi = dataWifi;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }
}
